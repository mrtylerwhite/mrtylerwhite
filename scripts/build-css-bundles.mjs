#!/usr/bin/env node
/**
 * Phase 2: flatten headings.css @import chain into one critical.css bundle.
 * Source files under assets/css/system/ stay editable; this output is generated.
 *
 * node scripts/build-css-bundles.mjs
 */

import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const ROOT = path.join(path.dirname(fileURLToPath(import.meta.url)), "..");
const OUT = path.join(ROOT, "assets/css/dist/critical.css");

/** Same dependency order as headings.css @import list, then headings rules last. */
const FRAGMENTS = [
  "assets/css/system/tokens.css",
  "assets/css/system/background.css",
  "assets/css/system/fonts.css",
  "assets/css/system/components/buttons.css",
  "assets/css/system/eyebrows.css",
  "assets/css/system/content-rhythm.css",
  "assets/css/system/mobile-open-menu.css",
  "assets/css/system/accessibility.css",
  "assets/css/system/headings.css",
];

const IMPORT_RE = /@import\s+url\(\s*["']?([^"')]+)["']?\s*\)\s*;?/g;

const FONT_PRELOAD =
  '<link rel="preload" href="/assets/fonts/instrument-serif/InstrumentSerif-Regular.woff2" as="font" type="font/woff2" crossorigin />\n';

function readCss(relPath) {
  return fs.readFileSync(path.join(ROOT, relPath), "utf8");
}

function stripImports(css) {
  return css.replace(IMPORT_RE, "").trim();
}

function applyFontPreload(html) {
  if (html.includes("InstrumentSerif-Regular.woff2")) return html;
  if (html.includes('/assets/css/dist/critical.css')) {
    return html.replace(
      /(<link\s+rel=["']stylesheet["']\s+href=["']\/assets\/css\/dist\/critical\.css["']\s*\/?>)/i,
      FONT_PRELOAD + "$1"
    );
  }
  if (html.includes('/assets/css/system/index.css')) {
    return html.replace(
      /(<link\s+rel=["']stylesheet["']\s+href=["']\/assets\/css\/system\/index\.css["']\s*\/?>)/i,
      FONT_PRELOAD + "$1"
    );
  }
  return html;
}

function walkHtml(dir, files = []) {
  for (const name of fs.readdirSync(dir)) {
    if (name === "node_modules" || name === ".git" || name === "wp-content" || name === "wp-includes") {
      continue;
    }
    const full = path.join(dir, name);
    const st = fs.statSync(full);
    if (st.isDirectory()) walkHtml(full, files);
    else if (name.endsWith(".html")) files.push(full);
  }
  return files;
}

function applyCriticalLink(html) {
  return html.replace(
    /<link\s+rel=["']stylesheet["']\s+href=["']\/assets\/css\/system\/headings\.css["']\s*\/?>\s*/gi,
    '<link rel="stylesheet" href="/assets/css/dist/critical.css" />\n'
  );
}

function buildBundle() {
  const chunks = [
    "/*",
    " * Generated by scripts/build-css-bundles.mjs — do not edit by hand.",
    " * Rebuild: npm run build:css",
    " */",
    "",
  ];

  for (const rel of FRAGMENTS) {
    const raw = readCss(rel);
    const body = stripImports(raw);
    chunks.push(`/* ----- ${rel} ----- */`, body, "");
  }

  fs.mkdirSync(path.dirname(OUT), { recursive: true });
  const out = `${chunks.join("\n").trim()}\n`;
  fs.writeFileSync(OUT, out, "utf8");
  return { bytes: Buffer.byteLength(out, "utf8"), path: OUT };
}

function patchIndexCss() {
  const indexPath = path.join(ROOT, "assets/css/system/index.css");
  let css = readCss("assets/css/system/index.css");
  const next = `/* Phase 1 system entrypoint (no build step for page CSS beyond critical bundle) */
@import url("/assets/css/dist/critical.css");
@import url("/assets/css/system/typography.css");
@import url("/assets/css/system/layout.css");
@import url("/assets/css/system/components/forms.css");
@import url("/assets/css/system/components/controls.css");
@import url("/assets/css/system/components/cards.css");
@import url("/assets/css/system/components/testimonials.css");
`;
  if (css !== next) {
    fs.writeFileSync(indexPath, next, "utf8");
    return true;
  }
  return false;
}

function patchHtmlLinks() {
  let htmlChanged = 0;
  for (const file of walkHtml(ROOT)) {
    let html = fs.readFileSync(file, "utf8");
    const linked = applyCriticalLink(html);
    const next = applyFontPreload(linked);
    if (next !== html) {
      fs.writeFileSync(file, next, "utf8");
      htmlChanged++;
      console.log("  patched →", path.relative(ROOT, file));
    }
  }
  return htmlChanged;
}

const bundle = buildBundle();
console.log(`Wrote ${path.relative(ROOT, bundle.path)} (${bundle.bytes} bytes)`);

const indexPatched = patchIndexCss();
if (indexPatched) console.log("Updated assets/css/system/index.css imports");

const htmlCount = patchHtmlLinks();
console.log(`Updated ${htmlCount} HTML file(s) to use critical.css`);
